func moveZeroes(nums []int) {
	// One pointer to track zero, other pointer to track non-zero element
	var i int
	for j := 0; j < len(nums); j++ {
		if nums[j] != 0 {
			nums[i], nums[j] = nums[j], nums[i]
			i++
		}
	}
}

